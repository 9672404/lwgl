package com.hlzt.power.model;

import com.hlzt.commons.model.BaseBean;

/**
 * 院内公告
 * @author user
 *
 */
public class PublicNotice extends BaseBean{
	private String title;//公告题目
	private String noticeType;//公告类型
	private String realName;//公告文件名
	private String filePath;//附件路径
	private String content;//公告内容
	private String top;//置顶（0 不置顶  1置顶）
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public String getTop() {
		return top;
	}
	public void setTop(String top) {
		this.top = top;
	}
	@Override
	public String toString() {
		return "PublicNotice [noticePath=" + filePath + ", realName="
				+ realName + ", title=" + title + ", top=" + top
				+ ", getNoticePath()=" + getFilePath() + ", getRealName()="
				+ getRealName() + ", getTitle()=" + getTitle() + ", getTop()="
				+ getTop() + ", getCreateDateStr()=" + getCreateDateStr()
				+ ", getCreateTime()=" + getCreateTime() + ", getCreateUser()="
				+ getCreateUser() + ", getDateTimeStr()=" + getDateTimeStr()
				+ ", getId()=" + getId() + ", getClass()=" + getClass()
				+ ", hashCode()=" + hashCode() + ", toString()="
				+ super.toString() + "]";
	}
	public String getNoticeType() {
		return noticeType;
	}
	public void setNoticeType(String noticeType) {
		this.noticeType = noticeType;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	
	
}
